/* ── Scene 5: GALAXY CORE ─ spiral arms + volumetric disk ──────────── */
NX.registerScene({n:'GALAXY CORE',c:'#ff6d00',fs: H+[
'float spiralDens(vec3 p){',
'  float r=length(p.xz),a=atan(p.z,p.x);',
'  float arm1=sin(a*2.-r*1.8+T*SP*.3+B*.5)*.5+.5;',
'  float arm2=sin(a*2.-r*1.8+PI+T*SP*.3)*.5+.5;',
'  float arms=pow(max(arm1,arm2),3.5);',
'  float diskH=exp(-p.y*p.y*(5.-B*1.2));',
'  float diskR=1.-smoothstep(0.,4.5+M*.5,r);',
'  float core=exp(-r*1.5-p.y*p.y*3.)*(2.+BT*.8);',
'  float noise=vn(p.xz*2.+T*.15)*.4+.6;',
'  return (arms*diskH*diskR+core)*noise;',
'}',
'void main(){',
'  vec2 sc=(uv-.5)*vec2(R.x/R.y,1.)*2.;',
'  float ca=T*SP*.06+MX.x*.4;',
'  float ce=.5+sin(T*SP*.04)*.2+MX.y*.3;',
'  vec3 ro=vec3(sin(ca)*cos(ce),sin(ce),cos(ca)*cos(ce))*6.;',
'  mat3 cm=camMat(ro,vec3(0),sin(T*SP*.03)*.05);',
'  vec3 rd=cm*normalize(vec3(sc,1.4));',
'  vec3 col=vec3(0.);float alpha=0.,td=0.;',
'  for(int i=0;i<80;i++){',
'    vec3 pp=ro+rd*td;',
'    pp.xz=rot2(T*SP*.02)*pp.xz;',
'    float dens=spiralDens(pp)*.08;',
'    if(dens>.001){',
'      float r=length(pp.xz);',
'      vec3 scol=pal(r*.12+atan(pp.z,pp.x)*.1+T*.05);',
'      scol*=(1.+BT*.5+FL*.3);',
'      col+=scol*dens*(1.-alpha);',
'      alpha+=dens*(1.-alpha);',
'      if(alpha>.97)break;',
'    }',
'    td+=.12+td*.015;',
'    if(td>18.)break;',
'  }',
'  vec2 starUV=sc*8.+vec2(T*SP*.02);',
'  float star=pow(h21(floor(starUV)),28.)*(1.+H*.5);',
'  col+=vec3(star)*(1.-alpha);',
'  col+=pal(T*.07)*exp(-length(sc)*2.)*.4*(1.+BT*.6);',
'  vec2 fUV=feedUV(1.025+B*.025,.005*SP,vec2(sin(T*SP*.15)*.002,0.));',
'  vec3 prev=texture2D(PV,fUV).rgb*.88;',
'  col=mix(prev,col,.25+B*.2);',
'  gl_FragColor=vec4(pow(sat(col),vec3(.87)),1.);',
'}'
].join('\n')});

